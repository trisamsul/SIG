				<h2>Metrics Measuring</h2>
				<p>The cases in this atlas show three different quantitative measurements of density: dwelling units per hectare or acre, people per hectare or acre, and floor area ratio. Although these are common measures of density, they are often used alone, without respect to the other measures. It is important to look at all three numbers to obtain an accurate depiction of density.<br/>
				The diagram below details the three measures.</p>
				
