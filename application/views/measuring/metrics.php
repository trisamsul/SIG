				<p>Why these three measures? These are the three most commonly used measurements of density and each describes density from a different perspective. Each measurement looks at a different aspect of density and alone, each measurement is used to plan for a specific set of needs. Urban planners often are most concerned with FAR measures; realtors are often most concerned with dwelling units due to their focus on renting and selling; and government agencies care most about population numbers, which are tied to city services and infrastructure needs. Although each measurement provides good information about a place, alone, they do not paint a complete picture of the density of a neighborhood. 
				A better understanding of the density of a place comes not only from the additional information supplied from each of the three measurements, but also through looking at the three measurements relative to each other. For example, two areas may have the same number of dwelling units, but may feel more or less dense based on the relationship of those units to the number of people and the FAR in the area.</p>
